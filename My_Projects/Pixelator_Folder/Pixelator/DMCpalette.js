  this.DMCColor = function(color){
    let colorArray = color;
    
  for(let i = 0; i < colorArray.length; i++){
      colorArray[i] = findClosestColor(colorArray[i],actualColors);
  }    
    return colorArray;
  }
  
  this.findClosestColor = function(inputColor, colorsArray){
    let closestColor = null;
    let closestDistance = Infinity;
    
    for(let i = 0; i < colorsArray.length; i++){
      let distance = colorDistance(inputColor, colorsArray[i]);
      
      if(distance < closestDistance){
        closestDistance = distance;
        closestColor = colorsArray[i];
      }
    }
    return closestColor;
    
  }
  
  this.colorDistance = function(color1, color2) {
  let rDiff = color1.r - color2.r;
  let gDiff = color1.g - color2.g;
  let bDiff = color1.b - color2.b;
  return sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff);
  }